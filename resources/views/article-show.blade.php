<h2 class="text-2xl font-light text-slate-900 dark:text-white mt-12 mb-6">Redefining Business Intelligence</h2>
<p>
                    True business intelligence is no longer about the dashboard; it’s about the decision. The future of ERP lies in its ability to democratize data. When every employee has access to context-aware insights at the point of action, the entire organization becomes more resilient.
